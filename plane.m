function [A,B,C,D] = plane(p1,p2,p3) % znajduje rownanie plaszczyny, argumenty to punkty nalezace do plaszczyzny
 A = det([p1(2),p1(3),1;...
          p2(2),p2(3),1;...
          p3(2),p3(3),1]);

 B = -det([p1(1),p1(3),1;...
           p2(1),p2(3),1;...
            p3(1),p3(3),1]);

 C = det([p1(1),p1(2),1;...
          p2(1),p2(2),1;...
          p3(1),p3(2),1]);

 D = -det([p1(1),p1(2),p1(3);...
          p2(1),p2(2),p2(3);...
          p3(1),p3(2),p3(3)]);

endfunction
